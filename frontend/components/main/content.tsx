function PinIcon({docId, docName}: {docId: number, docName: string}) {
    const [pinned, setPinned] = React.useState(false);

    useEffectExceptOnMount(() => {
    toast("Document pinned", {
        description: `${docName} document is ${pinned ? "pinned" : "unpinned"}.`,
        action: {
        label: "Undo",
        onClick: () => setPinned((prv) => !prv),
        },
    });
    }, [pinned]);

  return (
    <div className="max-w-8 "
        onClick={(e: any) => {
            e.preventDefault();
            setPinned(prv => !prv);
        }}>
        <svg
        width="24"
        height="24"
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth="2"
        strokeLinecap="round"
        strokeLinejoin="round"
        className={`lucide lucide-pin-icon text-foreground 
            ${pinned? "opacity-100 fill-primary": "opacity-50 fill-none"}`}
        xmlns="http://www.w3.org/2000/svg"
        >
        <g transform="rotate(44.30421,10.092912,11.49921)">
            <path d="M12 17v5" />
            <path
            d="M9 10.76a2 2 0 0 1-1.11 1.79l-1.78.9A2 2 0 0 0 5 15.24V16a1 1 0 0 0 1 1h12a1 1 0 0 0 1-1v-.76a2 2 0 0 0-1.11-1.79l-1.78-.9A2 2 0 0 1 15 10.76V7a1 1 0 0 1 1-1 2 2 0 0 0 0-4H8a2 2 0 0 0 0 4 1 1 0 0 1 1 1z"
            strokeWidth="2"
            />
        </g>
        </svg>
    </div>
  );
}

